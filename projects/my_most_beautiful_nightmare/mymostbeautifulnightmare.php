      <p>Reacting to the user’s touch, the interactive, flexible screen of the installation challenges the boundaries between physical and digital while capturing the adjustable character of the new sole. The interactive mesh was developed through a combination of visual programming, interactive sound, backlit projection and depth tracking. A cross platform concept, the installation was translated into three different applications. The projection on digital fabric lived first in London, Berlin, and Madrid during training sessions for Nike product specialists. The same visual treatment was deployed to Kinect-connected retail installations in Nike Town London. Finally, a didactic iPad interface, was spread to 40 product specialists across Europe, in order to train retail teams. All design of software and hardware, UX, and fixture product design (built in close collaboration with Thein & Rios) were developed in-house, in a time frame of only six weeks.</p>
