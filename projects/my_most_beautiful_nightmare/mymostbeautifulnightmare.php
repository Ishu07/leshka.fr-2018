    <h4>My most beautiful nightmare is a digital project that invites you to rediscover that dream that until today you simply can't forget.<br/><br/>

Trough this piece of digital poetry, I want to sensibilise people about the beauty in their deepest nightmare. They are not an escape, they are the communication between our conscious and unconscious. Dreams are an amazing language to learn that sadly doesn’t exist on duolingo. With the help of a psychoanalyst I put names and meanings on my most relevant dreams and linked them to my awaken situation.</h4>
