    <p class="lil-right">Taking a step away from traditional documentary formats, only the archive footage was used, coupled with graphic elements to illustrate the complex story told by a voiceover. The result walks the viewer through five key narratives and their historical relevance to the Olympics.</p>
    <p class="lil-right">The Equaliser invites people to see the effects of the five narratives on sprint performance, allowing users to interact with the data on their own terms. People can rewrite history by modifying the outcome of an existing 100-metre sprint, or choose to compose a new line-up from any athlete. Working with a data analyst, we parsed the complex information into a working system, which demonstrates the influence that each of these factors has on the individual athlete. By taking away or adding different factors, people begin to see that the athlete stands for more than just a runner in a race.</p>
