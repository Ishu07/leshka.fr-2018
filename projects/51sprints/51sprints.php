    <h4>
      51 Sprints, a documentary and run simulator<br/><br/>
In collaboration with the Nieuwe Instituut and Yuri Veerman, we created a video essay and interactive online experience. 51 Sprints uses the iconic 100-metre sprint as a starting point to visualise the complex web of narratives that lies behind the Olympic Games, through the individual athletes competing in it. By deconstructing the Games using media representation and data, we reveal the untold stories embedded within.
