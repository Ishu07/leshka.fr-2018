  <h2 class="introBio">I create high-end digital products combining smart concepts and beautiful designs with the best technology practices.</h2>
  <p>In 2017 I decided to be available as a freelancer in London to experience different environements and to expand my field of competences. At the same time that I've founded Where the Road-Ends, a company to put on trial my experiments and to connect the most talented professionals together, to build cutting-edges products.</p>
  <p>Before that, I was an Art Director at <a href="https://random.studio/">Random Studio</a> (Amsterdam), where I've started to get my hands on Digital Installations. I had the chance to be on stage with the rest of the team at <a href="http://resonate.io/2016/artists/random-studio/">Resonate 2016</a> in Belgrade. </p>
  <p>It was in 2013 that I've moved to London to work for <a href="https://vimeo.com/hireslondon">Hi-ReS!</a> where my role was to concept creative solutions for innovative experiences. It’s also the place where I've learned how to code rich experiences in webGL and co-produced <a href="http://void.hi-res.net/">V O iD</a>, <a href="http://void-ii.hi-res.net/">VOID-Pandora</a>. We had the opportunity to show these projects on stage for the <a href="https://medium.com/@Degordian/awwwards-conference-amsterdam-2016-review-6fdbf7c2dd2a">Awwwards conference 2015</a> in Amsterdam "Make the Internet great again".</p>
  <p>Prior to my experience in London, I've worked at <a href="https://www.werkstatt.fr/en/">Werkstatt</a> and <a href="http://www.03july.com/applications/">03July</a> in Paris for two years. During this period, I've focussed a lot on finding fine concepts for luxuary brands such as Dior and Kenzo. It was also the time to build my first Apps and discover the beauty of the <a href="http://i-remember.fr/en">more experimental websites</a>.</p>
  <p>Back in Lyon(FR), I've spent 7 years studying Art Direction, Visual Design and Technology at the Schools of Bellecour and Condé.</p>
